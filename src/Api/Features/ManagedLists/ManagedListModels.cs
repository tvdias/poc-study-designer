namespace Api.Features.ManagedLists;

// Create Managed List
public record CreateManagedListRequest(Guid ProjectId, string Name, string? Description);
public record CreateManagedListResponse(Guid Id, Guid ProjectId, string Name, string? Description, ManagedListStatus Status);

// Update Managed List
public record UpdateManagedListRequest(string Name, string? Description);
public record UpdateManagedListResponse(Guid Id, Guid ProjectId, string Name, string? Description, ManagedListStatus Status);

// Get Managed Lists
public record GetManagedListsResponse(
    Guid Id, 
    Guid ProjectId, 
    string Name, 
    string? Description, 
    ManagedListStatus Status,
    int ItemCount,
    int QuestionCount,
    DateTime CreatedOn,
    string? CreatedBy);

// Get Managed List by Id
public record GetManagedListByIdResponse(
    Guid Id, 
    Guid ProjectId, 
    string Name, 
    string? Description, 
    ManagedListStatus Status,
    DateTime CreatedOn,
    string? CreatedBy,
    DateTime? ModifiedOn,
    string? ModifiedBy,
    List<ManagedListItemDto> Items,
    string SourceType = "Question Bank",
    bool IsAutoGenerated = false,
    bool EverInSnapshot = false,
    DateTime? FirstSnapshotDate = null);

public record ManagedListItemDto(
    Guid Id, 
    string Code, 
    string Label, 
    int SortOrder, 
    bool IsActive, 
    string? Metadata,
    string SourceType = "Managed List",
    bool EverInSnapshot = false);

// Managed List Items
public record CreateManagedListItemRequest(string Code, string Label, int SortOrder, string? Metadata = null);
public record CreateManagedListItemResponse(Guid Id, Guid ManagedListId, string Code, string Label, int SortOrder, bool IsActive, string? Metadata);

public record UpdateManagedListItemRequest(string Code, string Label, int SortOrder, bool IsActive, string? Metadata = null);
public record UpdateManagedListItemResponse(Guid Id, Guid ManagedListId, string Code, string Label, int SortOrder, bool IsActive, string? Metadata);

// Bulk Operations
public record BulkManagedListItemInput(string Code, string Label, int SortOrder, bool IsActive = true, string? Metadata = null);

public record BulkAddOrUpdateManagedListItemsRequest(List<BulkManagedListItemInput> Items, bool AllowUpdates = true);

public record BulkOperationResult(
    int TotalRows,
    int InsertedCount,
    int UpdatedCount,
    int SkippedCount,
    int RejectedCount,
    List<BulkOperationRowResult> Results);

public record BulkOperationRowResult(
    int RowIndex,
    string Code,
    string Status,  // "inserted", "updated", "skipped", "rejected"
    string? ErrorMessage);

// Question Assignment
public record AssignManagedListToQuestionRequest(Guid QuestionnaireLineId, Guid ManagedListId);
public record AssignManagedListToQuestionResponse(Guid Id, Guid QuestionnaireLineId, Guid ManagedListId);
